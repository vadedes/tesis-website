                    <div bind="b9c8492e-a967-70d0-9218-e0d26c7c1369" class="accordion-description w-richtext">
                      <p bind="d6f0b0d3-5fc2-5507-209d-76d3c41a7d55">Pharmacogenomics is the study of genetic variations that influence an individual’s response to medications. Each person responds uniquely to treatment so the same approach may not be effective or may cause adverse side effects in other patients. Understanding this information is helpful to physicians in developing a strategy to optimize medicine therapies. Data with respect to a patient’s genotype is used to maximize medicine efficacy while minimizing adverse drug effects and drug-drug interactions.</p>
                      <p bind="edfb4691-2ba8-b4dc-fd98-340360ef6914">In Short, Pharmacogenomic (PGx) testing saves lives, by helping physicians prescribe the right medication for a patient taking the guesswork and the trial &amp; error out of prescribing and decreasing the time to proper treatment.</p>
                      <p bind="5c5bcd45-27f6-b1b4-52f6-bee1c82b07b1">The PGx Test can be useful for understanding the impact of the medications that are being used today but can also help for future treatments. This is because many commonly prescribed medicine are impacted by a handful of genes so there will be overlap for results and it is highly likely that the result of one PGx test will be useful for future treatments. Because a PGx test result can help to inform other treatment decisions in the future for a patient’s care, results should be shared with other providers and potentially even the patient’s pharmacist.</p>
                      <p bind="856054f0-e2d9-8e41-12ba-46e56dacdb9a">‍<strong bind="9256cb1d-945f-43db-8339-22d774b526e0">A single test designed to inform treatment decisions:</strong></p>
                      <p bind="76b36960-bbb2-5222-bbb7-14f0bbc1e731">✔ Patients demonstrating sensitivity or lack of symptom relief.</p>
                      <p bind="9ac7c87c-3b45-fb6e-a15b-53c1eec719de">✔ Patients with chronic conditions and multiple prescriptions.</p>
                      <p bind="ed003dc3-da29-2afe-0a90-29e0b0bf8c14">✔ Patients with a history of poor compliance.</p>
                      <p bind="1f822296-1e62-55d3-9805-65106021bc54">✔ Patients with pharmacotherapy failure.</p>
                      <p bind="a2b29d16-313e-051e-7515-5e430eb59029">✔ Patients with a personal or family history of adverse medical reactions in response to certain medications.</p>
